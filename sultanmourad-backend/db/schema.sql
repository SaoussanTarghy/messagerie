-- Database Schema for ISGA Group Chat

CREATE TABLE IF NOT EXISTS USER (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    USERNAME VARCHAR(50) UNIQUE NOT NULL,
    FIRST_NAME VARCHAR(50) NOT NULL,
    LAST_NAME VARCHAR(50) NOT NULL,
    EMAIL VARCHAR(100) UNIQUE NOT NULL,
    PASSWORD VARCHAR(64) NOT NULL, -- SHA-256 hash
    PERMISSION INT NOT NULL, -- 1: Admin, 2: Moderator, 3: Normal user
    LAST_CONNECTION_TIME TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    STATUS VARCHAR(20) DEFAULT 'offline', -- online, offline, away
    IS_BANNED BOOLEAN DEFAULT FALSE
);

CREATE TABLE IF NOT EXISTS MESSAGE (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    USER_ID INT,
    TIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONTENT TEXT NOT NULL,
    FOREIGN KEY (USER_ID) REFERENCES USER(ID)
);

CREATE TABLE IF NOT EXISTS LOG (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    USER_ID INT,
    TIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    TYPE VARCHAR(50) NOT NULL, -- login, logout, send message, ban, status change
    FOREIGN KEY (USER_ID) REFERENCES USER(ID)
);

